export function pixelIconCells(name) {
  const grid = PIXEL_ICONS[name]
  if (!grid) return []
  const cells = []
  grid.forEach((row, y) => {
    ;[...row].forEach((cell, x) => {
      if (cell === '#') cells.push([x, y])
    })
  })
  return cells
}

export function renderPixelIconSVG(name, { size = 16, color = 'currentColor' } = {}) {
  const cells = pixelIconCells(name)
  const unit = size / 8
  const rects = cells
    .map(([x, y]) => `<rect x="${x * unit}" y="${y * unit}" width="${unit}" height="${unit}" fill="${color}" />`)
    .join('')
  return `<svg width="${size}" height="${size}" viewBox="0 0 ${size} ${size}" shape-rendering="crispEdges" aria-hidden="true">${rects}</svg>`
}
